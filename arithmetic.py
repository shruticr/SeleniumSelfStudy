class Rational:
    """ A python class for basic arithmetic operations. With 2 rational
    numbers class can be used to add, subtract, divide and multiply.
    """

    def add(self, x, y):
    #  Calculates addition from  x and y and return the result.
         return x + y

    def subtract(self, x, y):
    # Calculates subtraction from  x and y and return the result.
        return x - y

    def divide(self, x, y):
    #  Calculates division from  x and y and return the result.
        return x / y

    def multiply(self, x, y):
    #  Calculates multiplication from  x and y and return the result.
        return x * y
